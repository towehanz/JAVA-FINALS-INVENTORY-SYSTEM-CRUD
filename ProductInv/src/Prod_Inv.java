    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String prodName = txtField_prodName.getText();
            String prodPrice = txtField_prodPrice.getText();
            String prodQty = txtField_prodQuantity.getText();
            
            prep = con.prepareStatement("insert into tbl_inv(prod_name, prod_price, prod_qty)values(?,?,?)");
            
            prep.setString(1,prodName);
            prep.setString(2, prodPrice);
            prep.setString(3, prodQty);
            
            int k = prep.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Product has been added to the inventory!");
                txtField_prodName.setText("");
                txtField_prodPrice.setText("");
                txtField_prodQuantity.setText("");
                
                txtField_prodName.requestFocus();
                loadProdID();
                
            } else {
                JOptionPane.showMessageDialog(this, "Sorry, you can't add this product.");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Prod_Inv.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String prodID = txt_prodID.getSelectedItem().toString();
        try {
            prep = con.prepareStatement("select * from tbl_inv where prod_id = ?");
            prep.setString(1, prodID);
            res = prep.executeQuery();
            
            if (res.next() == true){
                txtField_prodName.setText(res.getString(2));
                txtField_prodPrice.setText(res.getString(3));
                txtField_prodQuantity.setText(res.getString(4));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prod_Inv.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


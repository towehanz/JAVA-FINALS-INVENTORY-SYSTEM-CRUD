    public void loadProdID(){
        
        try {
            prep = con.prepareStatement("select prod_id from tbl_inv");
            res = prep.executeQuery();
            txt_prodID.removeAllItems();
            
            
            while(res.next()){
                txt_prodID.addItem(res.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prod_Inv.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
